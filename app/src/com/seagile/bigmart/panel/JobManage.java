    private void loadJobs() {
        try {
            ResultSet jobResult = MySQL.search("SELECT * FROM `job` INNER JOIN `status` ON `job`.`status_id` = `status`.`id` ORDER BY `job`.`id` ASC");

            DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
            tableModel.setRowCount(0);

            while (jobResult.next()) {
                Vector<String> jobList = new Vector();

                jobList.add(jobResult.getString("id"));
                jobList.add(jobResult.getString("job_title"));
                jobList.add(jobResult.getString("description"));
                String job_id = jobResult.getString("id");
                int enroleedEmployees = 0;
                try {
                    ResultSet employeeResult = MySQL.search("SELECT * FROM `employee` WHERE `job_id` = '" + job_id + "'");
                    while (employeeResult.next()) {
                        enroleedEmployees++;
                    }
                } catch (SQLException e) {
                    e.printStackTrace();
                }
                jobList.add(String.valueOf(enroleedEmployees));
                jobList.add(jobResult.getString("status_name"));
                tableModel.addRow(jobList);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void jobStatics() {
        try {
            ResultSet result = MySQL.search("SELECT * FROM `job`");
            int totalJobs = 0;
            int activeJobs = 0;
            int inactiveJobs = 0;
            int emptyJob = 0;
            while (result.next()) {
                totalJobs++;
                if ("1".equals(result.getString("status_id"))) {
                    activeJobs++;
                } else if ("2".equals(result.getString("status_id"))) {
                    inactiveJobs++;
                }
                String job_id = result.getString("id");
                try {
                    ResultSet employeeResult = MySQL.search("SELECT * FROM `employee` WHERE `job_id` = '" + job_id + "'");
                    if (!employeeResult.next()) {
                        emptyJob++;
                    }
                } catch (SQLException e) {
                    e.printStackTrace();
                }
            }

            jLabel2.setText(String.valueOf(totalJobs + " "));
            jLabel10.setText(String.valueOf(activeJobs + " "));
            jLabel12.setText(String.valueOf(inactiveJobs + " "));
            jLabel14.setText(String.valueOf(emptyJob + " "));

            loadJobs();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

