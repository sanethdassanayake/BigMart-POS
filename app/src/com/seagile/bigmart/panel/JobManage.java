    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String searchTerm = jTextField1.getText();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        try {
            ResultSet jobResult = MySQL.search("SELECT * FROM `job`"
                    + "INNER JOIN `status` ON `job`.`status_id` = `status`.`id`"
                    + "WHERE `job`.`job_title` LIKE '%" + searchTerm + "%' OR `job`.`description` LIKE '%" + searchTerm + "%' ORDER BY `job`.`id` ASC");

            while (jobResult.next()) {
                Vector<String> jobList = new Vector<>();
                jobList.add(jobResult.getString("id"));
                jobList.add(jobResult.getString("job_title"));
                jobList.add(jobResult.getString("description"));
                String job_id = jobResult.getString("id");
                int enroleedEmployees = 0;
                try {
                    ResultSet employeeResult = MySQL.search("SELECT * FROM `employee` WHERE `job_id` = '" + job_id + "'");
                    while (employeeResult.next()) {
                        enroleedEmployees++;
                    }
                } catch (SQLException e) {
                    e.printStackTrace();
                }
                jobList.add(String.valueOf(enroleedEmployees));
                jobList.add(jobResult.getString("status_name"));
                model.addRow(jobList);
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


